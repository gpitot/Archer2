/**
 * Builds a compact `server/navdata.ts` module holding every playable map:
 *  - 'arena': parsed from the WC3 binaries in `assets/`
 *  - 'test':  generated in code (src/world/testMap.ts)
 *
 * Pathing grids are stored as base64-encoded bit-packed Uint8Arrays (~60KB
 * for the arena) instead of verbose JSON boolean arrays (~1.5MB).
 *
 * Usage:  pnpm tsx scripts/build-navdata-compact.ts
 */
import * as fs from 'fs';
import * as path from 'path';
import { fileURLToPath } from 'url';
import { parseW3E, W3ETerrain } from '../src/world/wc3/W3EParser';
import { parseWpm, isCellWalkable, PATH_CELL_SIZE, WpmPathing } from '../src/world/wc3/WpmParser';
import { parseDoo, DoodadPlacement } from '../src/world/wc3/DooParser';
import { buildNavGridFromWpm, findWalkableNearOnGrid } from '../src/sim/buildWorld';
import { NavGrid } from '../src/navigation/NavGrid';
import { buildTestMapData, testMapObstacles, TEST_MAP_ARENA } from '../src/world/testMap';
import { parseMapJson } from '../src/world/custom/mapSource';
import { buildCustomMap } from '../src/world/custom/buildCustomMap';
import type { CampPlacement } from '../src/sim/creepRules';
import type { RunePlacement } from '../src/sim/runeRules';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ASSETS_DIR = path.resolve(__dirname, '..', 'assets');
const SERVER_DIR = path.resolve(__dirname, '..', 'server');

interface ObstacleAABB {
  minX: number;
  minZ: number;
  maxX: number;
  maxZ: number;
}

interface ArenaRect {
  minX: number;
  minZ: number;
  maxX: number;
  maxZ: number;
  centerX: number;
  centerZ: number;
  width: number;
  height: number;
}

function readFile(name: string): ArrayBuffer {
  const p = path.join(ASSETS_DIR, name);
  if (!fs.existsSync(p)) throw new Error(`missing asset: ${p}`);
  const buf = fs.readFileSync(p);
  return buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength);
}

function wc3ToWorld(x: number, y: number): { wx: number; wz: number } {
  return { wx: x, wz: -y };
}

function main() {
  const entries: string[] = [];

  // ── 'arena': the original WC3 map ──
  {
    const terrain = parseW3E(readFile('war3map.w3e'));
    const pathing = parseWpm(readFile('war3map.wpm'));
    const doodads = parseDoo(readFile('war3map.doo'));

    const tilesW = terrain.width - 1;
    const tilesH = terrain.height - 1;
    if (pathing.width !== tilesW * 4 || pathing.height !== tilesH * 4) {
      throw new Error('pathing/terrain mismatch');
    }

    // Obstacles: solid rocks only — trees don't block arrows.
    const ROCK_STYLES = new Set(['ARrk', 'LRrk', 'LPcr']);
    const obstacles: ObstacleAABB[] = [];
    for (const d of doodads) {
      if (!ROCK_STYLES.has(d.typeId)) continue;
      const { wx, wz } = wc3ToWorld(d.x, d.y);
      const col = Math.floor((d.x - terrain.offsetX) / PATH_CELL_SIZE);
      const row = Math.floor((d.y - terrain.offsetY) / PATH_CELL_SIZE);
      if (!isCellWalkable(pathing, col, row)) {
        const halfW = 40 * d.scaleX;
        const halfD = 40 * d.scaleY;
        obstacles.push({ minX: wx - halfW, minZ: wz - halfD, maxX: wx + halfW, maxZ: wz + halfD });
      }
    }

    const arena = arenaFromWc3(-2784, -6720, 4416, 512);
    entries.push(buildEntry('arena', terrain, pathing, doodads, arena, obstacles, null, null, null, null));
  }

  // ── 'test': the tiny generated debug map ──
  {
    const map = buildTestMapData();
    entries.push(buildEntry('test', map.terrain, map.pathing, map.doodads, { ...TEST_MAP_ARENA }, testMapObstacles(), null, null, null, null));
  }

  // ── Custom editor-made maps: every maps/*.map.json ──
  const mapsDir = path.resolve(__dirname, '..', 'maps');
  if (fs.existsSync(mapsDir)) {
    for (const file of fs.readdirSync(mapsDir).sort()) {
      if (!file.endsWith('.map.json')) continue;
      const name = file.replace('.map.json', '');
      if (name === 'arena' || name === 'test') {
        console.warn(`[build-navdata-compact] skipping maps/${file}: name shadows a built-in map`);
        continue;
      }
      const src = parseMapJson(fs.readFileSync(path.join(mapsDir, file), 'utf-8'));
      const custom = buildCustomMap(src);
      entries.push(buildEntry(
        name, custom.data.terrain, custom.data.pathing, custom.data.doodads,
        custom.arena, custom.obstacles, custom.camps, custom.spawns, custom.runes,
        custom.fountains.map((f) => ({ x: f.pos.x, z: f.pos.z })),
        custom.shops.map((s) => ({ x: s.x, z: s.z })),
      ));
    }
  }

  const ts = `/**
 * Compact navdata for the Durable Object server, one entry per map.
 * Auto-generated by scripts/build-navdata-compact.ts — do not edit.
 *
 * Pathing grids are bit-packed (1 = walkable).  Decode with:
 *   const byte = cellsBase64[i >> 3];
 *   const walkable = (byte >> (i & 7)) & 1;
 */
export const NAVDATA = {
${entries.join(',\n')}
} as const;

export type NavdataMapName = keyof typeof NAVDATA;
`;

  const outPath = path.join(SERVER_DIR, 'navdata.ts');
  fs.mkdirSync(SERVER_DIR, { recursive: true });
  fs.writeFileSync(outPath, ts, 'utf-8');
  console.log(`[build-navdata-compact] wrote ${outPath}  (${(Buffer.byteLength(ts) / 1024).toFixed(0)} KB)`);
}

/** Serialize one map's navdata entry (shared by every map source). */
function buildEntry(
  name: string,
  terrain: W3ETerrain,
  pathing: WpmPathing,
  doodads: DoodadPlacement[],
  arena: ArenaRect,
  obstacles: ObstacleAABB[],
  camps: CampPlacement[] | null = null,
  spawns: { x: number; z: number }[] | null = null,
  runes: RunePlacement[] | null = null,
  fountains: { x: number; z: number }[] | null = null,
  shops: { x: number; z: number }[] | null = null,
): string {
  const tilesH = terrain.height - 1;
  const bounds = { minX: terrain.offsetX, minZ: -(terrain.offsetY + tilesH * 128) };

  // Same code path as the client's buildSimWorld, so the grids are identical.
  const bare = buildNavGridFromWpm(pathing, bounds);
  const navGrid = buildNavGridFromWpm(pathing, bounds, doodads);
  logTreeStamping(name, bare, navGrid);

  // Bits stay in WPM-native order (row 0 = south); NavGrid row 0 = north.
  const numCells = pathing.width * pathing.height;
  const bytes = new Uint8Array(Math.ceil(numCells / 8));
  for (let i = 0; i < numCells; i++) {
    const row = Math.floor(i / pathing.width);
    const col = i % pathing.width;
    if (navGrid.isWalkable(col, pathing.height - 1 - row)) {
      bytes[Math.floor(i / 8)] |= 1 << (i % 8);
    }
  }
  const cellsBase64 = Buffer.from(bytes).toString('base64');

  // Full-resolution height grid (Float32Array → base64).
  const heightsBase64 = Buffer.from(new Uint8Array(terrain.finalHeight.buffer)).toString('base64');

  // Shop position (on the stamped grid, so it can't land inside a tree).
  // Use authored shop if available, else fall back to arena centre.
  const shopPos = shops && shops.length > 0
    ? findWalkableNearOnGrid(navGrid, shops[0].x, shops[0].z)
    : findWalkableNearOnGrid(navGrid, arena.centerX, arena.centerZ);

  const obsLines = obstacles.map((o) =>
    `      { minX: ${o.minX.toFixed(1)}, minZ: ${o.minZ.toFixed(1)}, maxX: ${o.maxX.toFixed(1)}, maxZ: ${o.maxZ.toFixed(1)} }`,
  );

  return `  ${JSON.stringify(name)}: {
    /** Bit-packed walkable cells, 1 bit per cell, row-major (south-to-north). */
    navGrid: {
      width: ${pathing.width},
      height: ${pathing.height},
      cellSize: ${PATH_CELL_SIZE},
      originX: ${terrain.offsetX},
      originZ: ${-(terrain.offsetY + tilesH * 128)},
      /** Base64-encoded Uint8Array of bit-packed walkable booleans. */
      cellsBase64: "${cellsBase64}",
    },

    /** Full-resolution height grid (Float32Array). */
    heightGrid: {
      width: ${terrain.width},
      height: ${terrain.height},
      /** Base64-encoded Float32Array of world-space elevations. */
      heightsBase64: "${heightsBase64}",
    },

    /** Solid doodad AABBs (projectile collision). */
    obstacles: [
${obsLines.join(',\n')}
    ],

    arena: ${JSON.stringify(arena)},

    shopPos: { x: ${shopPos.x.toFixed(1)}, z: ${shopPos.z.toFixed(1)} },

    /** Authored creep camps (custom maps); null → arena-fraction CAMP_DEFS. */
    camps: ${camps && camps.length > 0 ? JSON.stringify(camps.map((c) => ({ ...c, x: round1(c.x), z: round1(c.z) }))) : 'null'},

    /** Fixed hero spawns (custom maps); null → random walkable spawns. */
    spawns: ${spawns && spawns.length > 0 ? JSON.stringify(spawns.map((s) => ({ x: round1(s.x), z: round1(s.z) }))) : 'null'},

    /** Authored rune spots (custom maps); null → arena-fraction RUNE_SPOT_DEFS. */
    runes: ${runes && runes.length > 0 ? JSON.stringify(runes.map((r) => ({ x: round1(r.x), z: round1(r.z) }))) : 'null'},

    /** Authored fountain placements (custom maps); null → built-in defaults. */
    fountains: ${fountains && fountains.length > 0 ? JSON.stringify(fountains.map((f) => ({ x: round1(f.x), z: round1(f.z) }))) : 'null'},

    /** Authored shop placements (custom maps); null → arena-centre default. */
    shops: ${shops && shops.length > 0 ? JSON.stringify(shops.map((s) => ({ x: round1(s.x), z: round1(s.z) }))) : 'null'},
  }`;
}

// ── Helpers ───────────────────────────────────────────────────────────

function round1(n: number): number {
  return Math.round(n * 10) / 10;
}

function arenaFromWc3(minx: number, miny: number, maxx: number, maxy: number): ArenaRect {
  const minZ = -maxy;
  const maxZ = -miny;
  return {
    minX: minx, minZ, maxX: maxx, maxZ,
    centerX: (minx + maxx) / 2,
    centerZ: (minZ + maxZ) / 2,
    width: maxx - minx,
    height: maxZ - minZ,
  };
}

function countWalkable(grid: NavGrid): number {
  let n = 0;
  for (let gz = 0; gz < grid.height; gz++) {
    for (let gx = 0; gx < grid.width; gx++) {
      if (grid.isWalkable(gx, gz)) n++;
    }
  }
  return n;
}

function logTreeStamping(name: string, before: NavGrid, after: NavGrid): void {
  const total = before.width * before.height;
  const a = countWalkable(before);
  const b = countWalkable(after);
  console.log(
    `[build-navdata-compact] ${name}: tree stamping blocked ${a - b} cells ` +
    `(walkable ${((a / total) * 100).toFixed(1)}% → ${((b / total) * 100).toFixed(1)}%)`,
  );
}

main();
